-- FIND TOTAL CUSTOMERS BY COUNTRIES
SELECT
	Country,
	COUNT(customer_id) AS total_customers
FROM gold.dim_customers
GROUP BY Country


-- WHAT IS THE AVERAGE COSTS IN EACH Track ?
SELECT
	track_id,
	AVG(total_amount) AS avg_cost
FROM gold.fact_sales
GROUP BY track_id
ORDER BY AVG(total_amount) DESC


-- WHAT IS THE TOTAL REVENUE GENERATED FOR EACH Track ?
SELECT
	track_id,
	SUM(total_amount) AS total_revenue
FROM gold.fact_sales
GROUP BY track_id
ORDER BY SUM(total_amount) DESC


-- WHAT IS THE TOTAL REVENUE GENERATED BY EACH CUSTOMER ?
SELECT
	customer_id,
	SUM(total_amount) AS total_revenue
FROM gold.fact_sales
GROUP BY customer_id
ORDER BY SUM(total_amount) DESC


-- WHAT IS THE DISTRIBUTION OF SOLD ITEMS ACROSS COUNTRIES ?
SELECT
	c.Country,
	COUNT(s.quantity) AS total_items
FROM gold.fact_sales s
JOIN gold.dim_customers c
ON s.customer_id = c.customer_id
GROUP BY c.Country
ORDER BY COUNT(s.track_id) DESC


-- WHICH 5 TRACK GENERATE THE HIGHEST REVENUE ?
SELECT TOP 5
	t.track_name,
	SUM(s.total_amount) AS total_revenue
FROM gold.fact_sales s
JOIN gold.dim_track t
ON s.track_id = t.track_id
GROUP BY t.track_name
ORDER BY SUM(s.total_amount) DESC


-- WHAT ARE THE 5 WORST-PERFORMING TRACK IN TERMS OF SALES ?
SELECT TOP 5
	t.track_name,
	SUM(s.total_amount) AS total_revenue
FROM gold.fact_sales s
JOIN gold.dim_track t
ON s.track_id = t.track_id
GROUP BY t.track_name
ORDER BY SUM(s.total_amount)


-- FIND THE TOP 10 CUSTOMERS WHO HAVE GENERATED THE HIGHEST REVENUE
SELECT TOP 10
	customer_id,
	SUM(total_amount) AS total_revenue
FROM gold.fact_sales
GROUP BY customer_id
ORDER BY SUM(total_amount) DESC


-- FIND THE TOP 3 CUSTOMERS WITH FEWEST ORDERS PLACED
SELECT TOP 3
	customer_id,
	COUNT(DISTINCT invoice_id) AS total_orders
FROM gold.fact_sales
GROUP BY customer_id
ORDER BY COUNT(invoice_id)


-- Revenue per individual customer
WITH CTE AS
(
	SELECT
		c.customer_id AS CustomerId,
		c.Country AS Country,
		SUM(s.total_amount) AS total_revenue
	FROM gold.fact_sales s
	JOIN gold.dim_customers c
	ON s.customer_id = c.customer_id
	GROUP BY c.customer_id, c.Country
) 
SELECT 
	CustomerId,
	Country,
	total_revenue
FROM CTE
ORDER BY total_revenue DESC


-- Which country generates the highest revenue per customer?
WITH CTE AS
(
	SELECT
		c.Country AS Country,
		SUM(s.total_amount) AS total_revenue,
		COUNT(c.customer_id) AS TotalCustomers
	FROM gold.fact_sales s
	JOIN gold.dim_customers c
	ON s.customer_id = c.customer_id
	GROUP BY c.Country
) 
SELECT 
	TotalCustomers,
	Country,
	total_revenue * 1.0 / TotalCustomers AS revenue_per_customer
FROM CTE
ORDER BY total_revenue DESC


-- Which 3 tracks generate the most revenue in each country?
WITH CTE AS
(
	SELECT
		t.track_name AS TrackName,
		c.Country AS Country,
		SUM(s.total_amount) AS total_revenue
	FROM gold.fact_sales s
	JOIN gold.dim_customers c
	ON s.customer_id = c.customer_id
	JOIN gold.dim_track t
	ON s.track_id = t.track_id
	GROUP BY t.track_name, c.Country
),
Rankedsales AS (
	SELECT
		RANK() OVER(PARTITION BY Country ORDER BY total_revenue DESC) AS Row_Count,
		TrackName,
		Country,
		total_revenue
	FROM CTE
)
SELECT 
*
FROM Rankedsales
WHERE Row_Count <= 3
