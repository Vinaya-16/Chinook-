-- EXPLORE ALL OBJECTS IN THE DATABASE
SELECT * FROM INFORMATION_SCHEMA.TABLES;


-- EXPLORE ALL COLUMNS IN THE DATABASE
SELECT * FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'dim_customers';

-- EXPLORE ALL COUNTRIES OUR CUSTOMERS COME FROM
SELECT
	DISTINCT c.Country
FROM gold.dim_customers c


-- BASIC SALES OVERVIEW

-- Check row count
SELECT
	COUNT(*) AS total_sales
FROM gold.fact_sales

-- FIND THE DATE OF THE FIRST AND THE LAST ORDER
-- HOW MANY YEARS OF SALES ARE AVAILABLE
SELECT
	MIN(invoice_date) AS first_sale,
	MAX(invoice_date) AS last_sale,
	DATEDIFF(year, MIN(invoice_date), MAX(invoice_date)) AS year_of_sale
FROM gold.fact_sales

-- Total Revenue
SELECT
	SUM(total_amount) AS total_revenue
FROM gold.fact_sales;

-- Total sales count
SELECT
	COUNT(f.invoice_id) AS Total_sales
FROM gold.fact_sales f;

-- Average Order Value
SELECT
	AVG(total_amount) AS avg_order_value
FROM gold.fact_sales;

-- TIME-BASED ANALYSIS

-- Revenue by year
SELECT
	YEAR(invoice_date) AS sales_year,
	SUM(total_amount) AS yearly_revenue
FROM gold.fact_sales
GROUP BY YEAR(invoice_date)
ORDER BY YEAR(invoice_date)

-- Revenue by month
SELECT
	YEAR(invoice_date) AS sales_year,
	MONTH(invoice_date) AS sales_month,
	SUM(total_amount) AS monthly_sales
FROM gold.fact_sales
GROUP BY YEAR(invoice_date), MONTH(invoice_date)
ORDER BY sales_year, sales_month

-- EMPLOYEE PERFORMANCE

-- Top Performing Employee
SELECT
	CONCAT(e.FirstName, ' ',e.LastName) AS Employee_Name,
	SUM(f.quantity) AS total_quantities,
	COUNT(f.invoice_id) AS total_orders,
	SUM(f.total_amount) AS total_sales
FROM gold.fact_sales f
JOIN gold.dim_employees e
ON f.employee_id = e.employee_id
GROUP BY 
	e.FirstName,
	e.LastName
ORDER BY SUM(f.total_amount) DESC

-- Customer Analysis
SELECT
	f.customer_id,
	SUM(f.total_amount) AS total_spending,
	COUNT(DISTINCT f.invoice_id) AS total_orders,
	SUM(f.quantity) AS total_quantities
FROM gold.fact_sales f
GROUP BY f.customer_id
ORDER BY SUM(f.total_amount)

-- PRODUCT (TRACK) ANALYSIS

-- Top Selling Products
SELECT
	track_id,
	SUM(quantity) AS total_products_sold
FROM gold.fact_sales
GROUP BY track_id
ORDER BY SUM(quantity)


-- FIND HOW MANY ITEMS ARE SOLD
SELECT
	SUM(d.quantity) AS total_items_sold
FROM gold.fact_sales d


-- FIND THE TOTAL NO. OF CUSTOMERS THAT HAVE PLACED AN ORDER
SELECT
	COUNT(DISTINCT s.customer_id) AS customer_id
FROM gold.fact_sales s


-- GENERATE A REPORT THAT SHOWS ALL KEY METRICS OF THE BUSINESS

